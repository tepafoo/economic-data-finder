/**
 * Constructor.
 */
function DomUtil() {
}

/**
 * Creates a label.
 * 
 * @param text
 */
DomUtil.createLabel = function(text) {
	var label = document.createElement('div');
	label.innerText = text;

	return label;
}

/**
 * Creates an option element.
 * 
 * @param text
 * @param value
 * @returns
 */
DomUtil.createOptionElement = function(text, value) {
	var option = document.createElement('option');

	option.text = text;
	option.setAttribute('value', value);

	return option;
}